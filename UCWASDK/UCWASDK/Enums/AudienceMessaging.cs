namespace Microsoft.Skype.UCWA.Enums
{
    /// <summary>
    /// Represents the states of the messaging modality for all members of an audience (a conversation).
    /// </summary>
    public enum AudienceMessaging
    {
        /// <summary>
        /// Messaging is disabled for all members of the conversation.
        /// </summary>
        Disabled,
        /// <summary>
        /// Messaging is enabled for all members of the conversation.
        /// </summary>
        Enabled,
        /// <summary>
        /// The state is unknown.
        /// </summary>
        Unknown
    }
}
