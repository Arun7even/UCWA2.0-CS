namespace Microsoft.Skype.UCWA.Enums
{
    /// <summary>
    /// Represents whether meeting participants are allowed to join from a phone by dialing into the online meeting.
    /// </summary>
    public enum PhoneUserAdmission
    {
        /// <summary>
        /// Participants are not allowed to join by phone.
        /// </summary>
        Disabled,
        /// <summary>
        /// Participants are allowed to join by phone.
        /// </summary>
        Enabled
    }
}
