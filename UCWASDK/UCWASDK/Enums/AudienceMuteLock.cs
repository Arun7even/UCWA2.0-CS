namespace Microsoft.Skype.UCWA.Enums
{
    /// <summary>
    /// Represents mute status for both audio and video.
    /// </summary>
    public enum AudienceMuteLock
    {
        /// <summary>
        /// The media is not muted.
        /// </summary>
        Disabled,
        /// <summary>
        /// The mute lock is enabled.
        /// </summary>
        Enabled,
        /// <summary>
        /// Type of mute status is unknown.
        /// </summary>
        Unknown
    }
}
