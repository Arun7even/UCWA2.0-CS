namespace Microsoft.Skype.UCWA.Enums
{
    /// <summary>
    /// Represents the type of an audio transcript.
    /// </summary>
    public enum AudioTranscriptStatus
    {
        /// <summary>
        /// The connected event for the audio transcript.
        /// </summary>
        Connected,
        /// <summary>
        /// The ended event for the audio transcript.
        /// </summary>
        Ended,
        /// <summary>
        /// The transferred event for the audio transcript.
        /// </summary>
        Transferred,
        /// <summary>
        /// The audio transcript event type is unknown.
        /// </summary>
        Unknown
    }
}
